class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int N) {
        //code here
        vector<int> ans(2,0);
        int i=0,j=N-1;
        while(i<N && j>=0) {
            while(j>=0 && mat[i][j]==1) j--;
            if(N-j-1>ans[1]) {
                ans[1]=N-j-1;
                ans[0]=i;
            }
            i++;
        }
        return ans;
    }
};

